// Reset a user's password to a freshly hashed value. The plaintext
// is generated by the caller and returned to the admin once so they
// can hand it to the user. Never stored in plain text.
export function resetUserPassword(id, newPassword) {
  const db = getDb();
  const now = Date.now();
  const passwordHash = hashSync(newPassword, 10);
  db.update(users).set({ passwordHash, updatedAt: now }).where(eq(users.id, id)).run();
}

// ─── Invitations ───

function randomToken(len = 32) {
  const bytes = new Uint8Array(len);
  if (typeof crypto !== 'undefined' && crypto.getRandomValues) {
    crypto.getRandomValues(bytes);
  } else {
    for (let i = 0; i < len; i++) bytes[i] = Math.floor(Math.random() * 256);
  }
  return Array.from(bytes, (b) => b.toString(16).padStart(2, '0')).join('');
}

export function createInvitation({ email, role = 'user', invitedBy, expiresInDays = 7 }) {
  const db = getDb();
  const now = Date.now();
  const id = crypto.randomUUID();
  const token = randomToken(32);
  const expiresAt = now + expiresInDays * 24 * 60 * 60 * 1000;

  db.insert(invitations)
    .values({
      id,
      email: email.toLowerCase(),
      token,
      role,
      invitedBy,
      expiresAt,
      acceptedAt: null,
      createdAt: now,
    })
    .run();

  return { id, token, expiresAt };
}

