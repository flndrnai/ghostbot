import { eq, count, desc, and, isNull, gt } from 'drizzle-orm';
import { hashSync, compareSync } from 'bcrypt-ts';
import { getDb } from './index.js';
import { users, invitations } from './schema.js';

export function getUserCount() {
  const db = getDb();
  const result = db.select({ count: count() }).from(users).get();
  return result?.count ?? 0;
}

export function getFirstAdminUser() {
  const db = getDb();
  return db.select().from(users).where(eq(users.role, 'admin')).get();
}

export function getUserById(id) {
  const db = getDb();
  return db.select().from(users).where(eq(users.id, id)).get();
}

export function updateUserProfile(id, fields) {
  const db = getDb();
  const now = Date.now();
  const allowed = {};
  if (fields.firstName !== undefined) allowed.firstName = fields.firstName;
  if (fields.lastName !== undefined) allowed.lastName = fields.lastName;
  if (fields.country !== undefined) allowed.country = fields.country;
  if (fields.avatarDataUrl !== undefined) allowed.avatarDataUrl = fields.avatarDataUrl;
  if (fields.email !== undefined) allowed.email = String(fields.email).toLowerCase();
  if (Object.keys(allowed).length === 0) return;
  db.update(users).set({ ...allowed, updatedAt: now }).where(eq(users.id, id)).run();
}

export function getUserByEmail(email) {
  const db = getDb();
  return db
    .select()
    .from(users)
    .where(eq(users.email, email.toLowerCase()))
    .get();
}

export function createFirstUser(email, password) {
  const db = getDb();

  // Atomic transaction to prevent race conditions
  const result = db.transaction((tx) => {
    const existing = tx.select({ count: count() }).from(users).get();
    if (existing.count > 0) {
      return { error: 'Admin account already exists' };
    }

    const now = Date.now();
    const id = crypto.randomUUID();
    const passwordHash = hashSync(password, 10);

    tx.insert(users)
      .values({
        id,
        email: email.toLowerCase(),
        passwordHash,
        role: 'admin',
        owner: 1, // first user is always the owner
        createdAt: now,
        updatedAt: now,
      })
      .run();

    return { success: true, id };
  });

  return result;
}

export function verifyPassword(user, password) {
  return compareSync(password, user.passwordHash);
}

